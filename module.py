def area():
     length = int(input("Please enter the length of the house: "))
     width = int(input("Please enter the width of the house: "))
     area = length * width
     print(area)
area()

def circumference():
     diameter = int(input("What is the diameter of the circle in inches?: "))
     pi = 3.14
     circumference = pi * diameter
     print(circumference)
circumference()

